function logNumberName(number) {
    if (number === 1) {
        console.log('один');
    }

    if (number === 2) {
        console.log('два');
    }

    if (number === 3) {
        console.log('три');
    }

    if (number === 4) {
        console.log('четыре');
    }

    if (number === 5) {
        console.log('пять');
    }

    if (number === 6) {
        console.log('шесть');
    }

    if (number === 7) {
        console.log('семь');
    }

    if (number === 8) {
        console.log('восемь');
    }

    if (number === 9) {
        console.log('девять');
    }
}

logNumberName(8);
